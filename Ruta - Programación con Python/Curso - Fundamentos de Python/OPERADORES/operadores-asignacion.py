# Operadores Asignación

x = 5
print(x)
x += 3 # Suma
print("Suma:", x)
x -= 3 # Resta
print("Resta:", x)
x *= 3 # Multiplicación
print("Multiplicación:", x)
x /= 3 # División
print("División:", x)
x %= 2 # Módulo(resto)
print("Módulo:", x)

y = 20
print
y //= 2 # División Entera
print("División Entera:", y)
y **= 3 # Exponente 10 * 10 * 10
print("Exponente:", y)

# WALRUS (morsa) :=
print(z := 3)
print(z)